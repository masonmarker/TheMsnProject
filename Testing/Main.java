class Main {
  public static void main(String[] args) {
    
    int n = 0;
    
    
    for (int i = 0; i < 2; i++) {
      for (int j = 0; j < 2; j++) {
        for (int k = 0; k < 2; k++) {
          for (int l = 0; l < 2; l++) {
            n++;
          }
        }
      }
    }
    
    System.out.println(n);
    
    
  }
}
